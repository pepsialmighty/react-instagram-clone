import { Input, Button } from "@material-ui/core";
import {
  createMuiTheme,
  withStyles,
  makeStyles,
  ThemeProvider,
} from "@material-ui/core/styles";
import { green, purple } from "@material-ui/core/colors";
import { storage, db } from "../firebase";
import firebase from "firebase";

import "./ImageUpload.css";

